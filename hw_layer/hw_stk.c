/*- Header files -------------------------------------------------------------*/
#include "ch32v10x.h"


/*!****************************************************************************
 * @brief
 * Enable SysTick-Timer
 *
 * @date  17.02.2022
 ******************************************************************************/
void vInitHW_STK(void)
{
  SysTick->CTLR |= 1UL;
}

/*!****************************************************************************
 * @brief
 * Get lower 32 bits of SysTick counter value
 *
 * @date  17.02.2022
 ******************************************************************************/
uint32_t ulGetTicks(void)
{
  return *(const volatile uint32_t*)&SysTick->CNTL0;
}
